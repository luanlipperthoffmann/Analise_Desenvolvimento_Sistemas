# 27. Área de um Paralelogramo: Solicite ao usuário a base e a altura de um paralelogramo. Calcule a área. Mostre a área calculada.

base = float (input("Dígite a base do paralelogramo: "))
altura = float (input("Dígite a altura do paralelogramo: "))
area = base*altura
print(f"A área do paralelogramo é de: {area}")