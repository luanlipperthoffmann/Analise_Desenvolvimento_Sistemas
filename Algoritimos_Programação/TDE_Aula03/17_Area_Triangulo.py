# 17. Área de um Triângulo: Solicite ao usuário a base e a altura de um triângulo. Calcule a área com Área = (base * altura) / 2. Mostre a área calculada.

base = float (input("Dígite a base do triângulo: "))
altura = float (input("Dígite a altura de um triângulo: "))
area = (base*altura)/2 
print(f"A área do triangulo é de: {area}")