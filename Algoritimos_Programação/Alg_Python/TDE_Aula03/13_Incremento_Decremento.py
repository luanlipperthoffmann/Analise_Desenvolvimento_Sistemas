# 13. Incremento e Decremento: Peça ao usuário um número. Incremente e decremente este número. Mostre ambos os resultados.

numero = int (input("Dígite um número: "))
incremento = numero + 1
decremento = numero - 1
print(f"O resultado do indremento do número dígitado é: {incremento}. Já o resultado do decremento é: {decremento}")